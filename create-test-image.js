const fs = require('fs');

// Create a minimal valid PNG file (1x1 pixel, black)
const pngHeader = Buffer.from([
  0x89, 0x50, 0x4e, 0x47, 0x0d, 0x0a, 0x1a, 0x0a,  // PNG signature
  0x00, 0x00, 0x00, 0x0d, 0x49, 0x48, 0x44, 0x52,  // IHDR chunk length and type
  0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,  // width=1, height=1
  0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,  // bit depth, color type, etc.
  0xde, 0x00, 0x00, 0x00, 0x0c, 0x49, 0x44, 0x41,  // IDAT chunk length and type
  0x54, 0x08, 0xd7, 0x63, 0x60, 0x60, 0x00, 0x00,  // compressed pixel data
  0x00, 0x02, 0x00, 0x01, 0xe2, 0x21, 0xbc, 0x33,  // more compressed data
  0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4e, 0x44,  // IEND chunk type
  0xae, 0x42, 0x60, 0x82                           // IEND CRC
]);

fs.writeFileSync('test-image.png', pngHeader);
console.log('Created test-image.png');
